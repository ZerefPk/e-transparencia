        controle social das atividades no âmbito do Conselho Regional de Contabilidade do Estado de Rondônia e à sociedade a informações sobre os atos de gestão praticados pelo Regional acerca de organização, despesas, receitas, licitações, demonstrações contábeis, relatórios de gestão, atos normativos e outras.

